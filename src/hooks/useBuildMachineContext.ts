                "open_browser",
                "start_dev_server",
                "stop_dev_server",
                "get_browser_errors",
                "get_dev_server_info",
                "save_image",
                "download_image",
                "ask_user",
                "set_mode",
                "request_agent_mode",
                "get_plan",
                "save_plan",
                "set_todo",
                "check_todo",
                "search_conversation",
